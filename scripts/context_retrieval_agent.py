class SourcegraphClient:
    """Minimal Sourcegraph API client for deep search and keyword search."""

    def __init__(self, url: str = "", token: str = ""):
        self.url = url or os.environ.get(
            "SOURCEGRAPH_URL",
            os.environ.get("SRC_ENDPOINT", "https://sourcegraph.sourcegraph.com"),
        )
        self.token = token or os.environ.get(
            "SOURCEGRAPH_ACCESS_TOKEN",
            os.environ.get("SRC_ACCESS_TOKEN", ""),
        )
        if not self.token:
            log.warning("No Sourcegraph token — deepsearch/hybrid backends unavailable")
        else:
            # Validate token at init
            self._validate_token()

    def _headers(self) -> dict:
        """Standard headers for SG API requests (includes User-Agent to avoid Cloudflare 1010)."""
        return {
            "Authorization": f"token {self.token}",
            "Content-Type": "application/json",
            "User-Agent": "ccb-context-retrieval-agent/1.0",
        }

    def _validate_token(self) -> None:
        """Check that the token is valid by running a simple query."""
        import urllib.request
        import urllib.error

        gql = 'query { currentUser { username } }'
        body = json.dumps({"query": gql}).encode()
        req = urllib.request.Request(
            f"{self.url.rstrip('/')}/.api/graphql",
            data=body,
            headers=self._headers(),
        )
        try:
            with urllib.request.urlopen(req, timeout=10) as resp:
                data = json.loads(resp.read())
                user = data.get("data", {}).get("currentUser", {})
                if user:
                    log.info("SG auth OK (user: %s)", user.get("username", "?"))
                else:
                    log.warning(
                        "SG token may be invalid — currentUser returned null. "
                        "SG search tools will fail with 403."
                    )
                    self.token = ""  # Disable SG tools
        except (urllib.error.URLError, Exception) as e:
            log.warning("SG token validation failed: %s. SG search tools disabled.", e)
            self.token = ""  # Disable SG tools

    def keyword_search(self, query: str, max_results: int = 50) -> str:
        """Run a keyword search and return formatted results."""
        import urllib.request
        import urllib.error

        gql = """query KeywordSearch($query: String!) {
            search(query: $query, version: V2) {
                results {
                    results {
                        ... on FileMatch {
                            repository { name }
                            file { path }
                            lineMatches { lineNumber preview }
                        }
                    }
                }
            }
        }"""
        full_query = f"{query} count:{max_results}"
        body = json.dumps({"query": gql, "variables": {"query": full_query}}).encode()
        req = urllib.request.Request(
            f"{self.url.rstrip('/')}/.api/graphql",
            data=body,
            headers=self._headers(),
        )
        try:
            with urllib.request.urlopen(req, timeout=30) as resp:
                data = json.loads(resp.read())
        except (urllib.error.URLError, Exception) as e:
            return f"Search error: {e}"

        results = data.get("data", {}).get("search", {}).get("results", {}).get("results", [])
        lines = []
        for r in results[:max_results]:
            repo = r.get("repository", {}).get("name", "?")
            path = r.get("file", {}).get("path", "?")
            matches = r.get("lineMatches", [])
            for m in matches[:3]:
                ln = m.get("lineNumber", 0)
                preview = m.get("preview", "").strip()[:200]
                lines.append(f"{repo}:{path}:{ln}: {preview}")
        return "\n".join(lines) if lines else "No results found."

    def deep_search(self, query: str, repos: List[str] = None) -> str:
        """Run a deep search query. Returns formatted summary."""
        # Deep search uses the streaming chat API which is complex.
        # Fallback: use NLS (natural language search) via keyword endpoint
        # with patternType:keyword for linguistic matching.
        repo_filter = ""
        if repos:
            repo_clauses = " ".join(f"repo:{r}" for r in repos)
            repo_filter = f"{repo_clauses} "
        full_query = f"{repo_filter}{query} patternType:keyword"
        return self.keyword_search(full_query, max_results=100)


def execute_deep_search(query: str, repos: List[str] = None, sg: "SourcegraphClient" = None) -> str:
    """Execute a deep search tool call."""
    if not sg or not sg.token:
        return "Deep search unavailable: no Sourcegraph token configured."
    return sg.deep_search(query, repos or [])


def execute_sourcegraph_search(query: str, sg: "SourcegraphClient" = None) -> str:
    """Execute a Sourcegraph keyword search tool call."""
    if not sg or not sg.token:
        return "Sourcegraph search unavailable: no token configured."
    return sg.keyword_search(query)

