def _infer_primary_repo(ctx: Dict[str, Any]) -> str:
    """Infer the primary repository name from task context."""
    # From Dockerfile env vars
    docker_envs = ctx.get("docker_env_vars", {})
    repo_name = docker_envs.get("SOURCEGRAPH_REPO_NAME", "")
    if repo_name:
        return repo_name
    # From clone URLs
    urls = ctx.get("repo_urls", [])
    if urls and isinstance(urls[0], dict):
        return urls[0].get("slug", urls[0].get("name", ""))
    if urls and isinstance(urls[0], str):
        return urls[0]
    return ""


def _convert_to_ir_schema(
    raw_oracle: Dict[str, Any],
    ctx: Dict[str, Any],
) -> Dict[str, Any]:
    """Convert agent output to the IR pipeline's ground_truth.json schema.

    Target format matches schemas/retrieval_events_schema.json ground_truth block:
      {files: [string], symbols: [{file, symbol, repo?}], expected_edit_files: [string],
       chunks: [{file, line_start, line_end, annotation?}]}

    Handles both new format (files as strings) and legacy (files as {repo, path} dicts).
    """
    # Normalize files to plain string paths
    files = []
    for f in raw_oracle.get("files", []):
        if isinstance(f, str):
            files.append(f)
        elif isinstance(f, dict):
            repo = f.get("repo", "")
            path = f.get("path", "")
            if ctx.get("is_multi_repo") and repo:
                clean = repo.replace("sg-evals/", "")
                if "--" in clean:
                    clean = clean.split("--")[0]
                files.append(f"{clean}::{path}")
            else:
                files.append(path)
    # Deduplicate preserving order
    seen = set()
    deduped = []
    for f in files:
        if f and f not in seen:
            seen.add(f)
            deduped.append(f)
    files = deduped

    # Normalize expected_edit_files
    edit_files = []
    for f in raw_oracle.get("expected_edit_files", []):
        if isinstance(f, str):
            edit_files.append(f)
        elif isinstance(f, dict):
            edit_files.append(f.get("path", ""))
    edit_files = [f for f in edit_files if f]

    # If agent didn't produce expected_edit_files, infer from test.sh
    if not edit_files and ctx.get("test_sh_diff_targets"):
        edit_files = ctx["test_sh_diff_targets"]

    # Normalize symbols to {file, symbol, repo}
    symbols = []
    for s in raw_oracle.get("symbols", []):
        if isinstance(s, dict):
            symbols.append({
                "file": s.get("path", s.get("file", "")),
                "symbol": s.get("symbol", ""),
                "repo": s.get("repo") if ctx.get("is_multi_repo") else None,
            })

    # Normalize chunks
    chunks = []
    for c in raw_oracle.get("chunks", []):
        if isinstance(c, dict) and "file" in c and "line_start" in c and "line_end" in c:
            chunks.append({
                "file": c["file"],
                "line_start": c["line_start"],
                "line_end": c["line_end"],
                "annotation": c.get("annotation"),
            })

    # Build result
    result: Dict[str, Any] = {"files": files}
    if symbols:
        result["symbols"] = symbols
    if edit_files:
        # Determine confidence
        if ctx.get("test_sh_diff_targets") and edit_files:
            result["expected_edit_files"] = edit_files
            result["expected_edit_files_source"] = "curator_agent_verifier_analysis"
            result["expected_edit_files_confidence"] = "high"
        else:
            result["expected_edit_files"] = edit_files
            result["expected_edit_files_source"] = "curator_agent_inference"
            result["expected_edit_files_confidence"] = "medium"
    if chunks:
        result["chunks"] = chunks

    # Preserve dependency_chain if present
    chain = raw_oracle.get("dependency_chain", [])
    if chain:
        result["dependency_chain"] = chain

    return result


def _convert_to_oracle_schema(
    raw_oracle: Dict[str, Any],
    ctx: Dict[str, Any],
) -> Dict[str, Any]:
    """Convert agent output to oracle_answer.json format for MCP artifact verifier.

    Target format: {files: [{repo, path}], symbols: [{repo, path, symbol}],
                    chain: [...], text: str}
    """
    primary_repo = _infer_primary_repo(ctx)

    # Normalize files to {repo, path} dicts
    files = []
    for f in raw_oracle.get("files", []):
        if isinstance(f, dict) and "repo" in f and "path" in f:
            files.append(f)
        elif isinstance(f, str):
            if "::" in f:
                repo, path = f.split("::", 1)
                files.append({"repo": repo, "path": path})
            else:
                files.append({"repo": primary_repo, "path": f})

    # Normalize symbols
    symbols = []
    for s in raw_oracle.get("symbols", []):
        if isinstance(s, dict):
            symbols.append({
                "repo": s.get("repo", primary_repo),
                "path": s.get("path", s.get("file", "")),
                "symbol": s.get("symbol", ""),
            })

    # Normalize chain
    chain_raw = raw_oracle.get("chain", raw_oracle.get("dependency_chain", []))
    chain = []
    for c in chain_raw:
        if isinstance(c, dict):
            chain.append(c)
        elif isinstance(c, str):
            chain.append({"repo": primary_repo, "path": c, "symbol": ""})

    result: Dict[str, Any] = {"files": files}
    if symbols:
        result["symbols"] = symbols
    if chain:
        result["chain"] = chain
    result["text"] = raw_oracle.get("text", "")

    return result


def build_user_message(
    ctx: Dict[str, Any], repo_paths: Dict[str, Path]
) -> str:
    """Build the user message for the agent."""
    parts = []

    # Task description
    seed = ctx.get("seed_prompt", "")
    instruction = ctx.get("instruction", "")
    if seed:
        parts.append(f"## Task\n{seed}")
    elif instruction:
        parts.append(f"## Task\n{instruction[:4000]}")
    else:
        parts.append(f"## Task\nAnalyze task: {ctx.get('task_name', 'unknown')}")

    # Check types
    check_types = ctx.get("check_types", [])
    if check_types:
        parts.append(f"\n## Expected Output Types\n{', '.join(check_types)}")

    # Available repos — extract clean repo names for Deep Search queries
    repo_names = set()
    parts.append("\n## Repositories")
    if repo_paths:
        for name, path in sorted(repo_paths.items()):
            parts.append(f"- **{name}**: `{path}`")
            # Extract clean GitHub org/repo name for Deep Search
            # e.g. "sg-evals/kubernetes--v1.32.0" -> "kubernetes/kubernetes"
            # e.g. "django/django" -> "django/django"
            clean = name.replace("sg-evals/", "")
            # Strip version suffix: "kubernetes--v1.32.0" -> "kubernetes"
            if "--" in clean:
                clean = clean.split("--")[0]
            repo_names.add(clean)
    else:
        parts.append("- *(none cloned locally)*")

    # Infer repo names from task context if not available from paths
    instruction = ctx.get("instruction", "") + " " + ctx.get("seed_prompt", "")
    for pattern in re.findall(r'github\.com/([a-zA-Z0-9_-]+/[a-zA-Z0-9_.-]+)', instruction):
        repo_names.add(pattern.rstrip("."))

    if repo_names:
        parts.append(
            f"\n**Use these repo names in Deep Search queries**: "
            f"{', '.join(sorted(repo_names))}"
        )

    parts.append(
        "\n**IMPORTANT**: The repos listed above may NOT be complete. "
        "The task may involve additional repositories not cloned locally. "
        "Use Deep Search and keyword search to discover files "
        "in repositories beyond the local set. Do not assume local repos are exhaustive."
    )

    # Task type and verifier hints (from Phase 0 parsing)
    task_type = ctx.get("task_type", "")
    suite = ctx.get("suite_name", "")
    if suite:
        parts.append(f"\n## Suite: {suite}")
    if task_type:
        parts.append(f"Task type: {task_type}")

    # Verifier file targets — helps agent determine expected_edit_files
    diff_targets = ctx.get("test_sh_diff_targets", [])
    if diff_targets:
        parts.append(
            "\n## Verifier Targets (files checked by test.sh)\n"
            + "\n".join(f"- `{t}`" for t in diff_targets)
        )
        parts.append(
            "These files are checked by the verifier's `git diff` — they are likely "
            "the files that need modification (expected_edit_files)."
        )

    return "\n".join(parts)


def run_agent(
    ctx: Dict[str, Any],
    repo_paths: Dict[str, Path],
    client: "anthropic.Anthropic",
    model: str = DEFAULT_MODEL,
    backend: str = "hybrid",
    sg: Optional["SourcegraphClient"] = None,
    verbose: bool = False,
) -> Tuple[Dict[str, Any], Dict[str, Any]]:
    """Run the retrieval agent on a single task.

    Args:
        ctx: Task context from load_task_context().
        repo_paths: Mapping of repo name -> local path.
        client: Anthropic API client.
        model: Model to use (default: claude-sonnet-4-6).
        backend: Tool backend: "local", "deepsearch", or "hybrid".
        sg: SourcegraphClient for deepsearch/hybrid backends.
        verbose: Enable verbose logging.

    Returns:
        (oracle_result, metadata) where oracle_result is the JSON output
        and metadata has cost/timing info.
    """
    system = CURATOR_SYSTEM_PROMPT.format(
        tool_description=_tool_description_for_backend(backend, cli_mode=False),
    )
    tools = get_tools_for_backend(backend)
    user_msg = build_user_message(ctx, repo_paths)
    messages = [{"role": "user", "content": user_msg}]

    total_input_tokens = 0
    total_output_tokens = 0
    tool_call_count = 0
    start_time = time.time()

    for _turn in range(MAX_TOOL_CALLS + 5):
        elapsed = time.time() - start_time
        if elapsed > TASK_TIMEOUT_SEC:
            log.warning("Task timeout after %.0fs", elapsed)
            break
        if tool_call_count >= MAX_TOOL_CALLS:
            log.warning("Max tool calls (%d) reached", MAX_TOOL_CALLS)
            messages.append({
                "role": "user",
                "content": (
                    "You have used all available tool calls. Please provide "
                    "your final JSON answer now based on what you've found so far."
                ),
            })

        try:
            response = client.messages.create(
                model=model,
                max_tokens=MAX_TOKENS,
                system=system,
                tools=tools,
                messages=messages,
            )
        except Exception as e:
            log.error("API error: %s", e)
            break

        total_input_tokens += response.usage.input_tokens
        total_output_tokens += response.usage.output_tokens

        assistant_content = response.content
        messages.append({"role": "assistant", "content": assistant_content})

        if response.stop_reason == "end_turn":
            break

        if response.stop_reason == "tool_use":
            tool_results = []
            for block in assistant_content:
                if block.type == "tool_use":
                    tool_call_count += 1
                    if verbose:
                        log.info(
                            "  Tool[%d]: %s(%s)",
                            tool_call_count, block.name,
                            json.dumps(block.input)[:200],
                        )
                    result = dispatch_tool(block.name, block.input, sg=sg)
                    if verbose:
                        log.info("  -> %s", result[:200])
                    tool_results.append({
                        "type": "tool_result",
                        "tool_use_id": block.id,
                        "content": result,
                    })
            messages.append({"role": "user", "content": tool_results})
        else:
            break

    oracle_result = _extract_json_from_messages(messages)

    elapsed = time.time() - start_time
    costs = MODEL_COSTS.get(model, MODEL_COSTS[DEFAULT_MODEL])
    cost = (
        total_input_tokens * costs["input"] / 1_000_000
        + total_output_tokens * costs["output"] / 1_000_000
    )

    metadata = {
        "generator": "context_retrieval_agent",
        "model": model,
        "backend": backend,
        "input_tokens": total_input_tokens,
        "output_tokens": total_output_tokens,
        "tool_calls": tool_call_count,
        "elapsed_sec": round(elapsed, 1),
        "cost_usd": round(cost, 4),
        "timestamp": datetime.now(timezone.utc).isoformat(),
    }

    return oracle_result, metadata


