#!/usr/bin/env python3
"""Context retrieval agent with multiple backends for oracle cross-validation.

Generates oracle ground truth files by running a Claude-powered agent with
configurable tool backends:

  local     - grep, ripgrep, find, file reading (no external services)
  deepsearch - Sourcegraph Deep Search (mcp__sourcegraph__deepsearch)
  hybrid    - both local tools AND deep search (default)

Cross-validation compares oracles generated by different backends. If
agreement between local-only and deepsearch-only is high (>80% file F1),
the circularity concern is empirically defused.

Environment variables:
    ANTHROPIC_API_KEY           Required. Claude API key.
    SOURCEGRAPH_ACCESS_TOKEN    Required for deepsearch/hybrid backends.
    SOURCEGRAPH_URL             SG instance (default: https://sourcegraph.sourcegraph.com)
    CCB_REPO_CACHE              Repo clone cache dir (default: ~/.cache/ccb_repos)

Usage:
    # Single task with hybrid backend (default)
    python3 scripts/context_retrieval_agent.py \\
        --task-dir benchmarks/ccb_mcp_crossrepo_tracing/ccx-dep-trace-001

    # Local-only backend (for cross-validation against SG oracle)
    python3 scripts/context_retrieval_agent.py \\
        --task-dir benchmarks/ccb_fix/kafka-producer-bufpool-fix-001 \\
        --backend local

    # Cross-validate: run all backends on same tasks, compare agreement
    python3 scripts/context_retrieval_agent.py \\
        --task-dir benchmarks/ccb_mcp_crossrepo_tracing/ccx-dep-trace-001 \\
        --cross-validate

    # Cheaper model for batch runs
    python3 scripts/context_retrieval_agent.py --sdlc-all \\
        --model claude-sonnet-4-6 --max-cost 20.00

    # Dry run
    python3 scripts/context_retrieval_agent.py --task-dir <dir> --dry-run
"""

