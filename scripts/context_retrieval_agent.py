def dispatch_tool(
    name: str, input_args: Dict[str, Any], sg: Optional["SourcegraphClient"] = None
) -> str:
    """Dispatch a tool call to the appropriate handler."""
    if name == "run_shell":
        return execute_run_shell(
            input_args.get("command", ""),
            input_args.get("cwd", ""),
        )
    elif name == "read_file":
        # Coerce start_line/end_line to int — models sometimes pass strings
        def _to_int(val: Any, default: int = 0) -> int:
            if isinstance(val, int):
                return val
            try:
                return int(val)
            except (TypeError, ValueError):
                return default
        return execute_read_file(
            input_args.get("path", ""),
            _to_int(input_args.get("start_line", 0)),
            _to_int(input_args.get("end_line", 0)),
        )
    elif name == "list_directory":
        return execute_list_directory(
            input_args.get("path", ""),
            input_args.get("pattern", ""),
            input_args.get("recursive", False),
        )
    elif name == "search_imports":
        return execute_search_imports(
            input_args.get("repo_path", ""),
            input_args.get("import_pattern", ""),
            input_args.get("language", ""),
        )
    elif name == "find_symbols":
        return execute_find_symbols(
            input_args.get("repo_path", ""),
            input_args.get("symbol_name", ""),
            input_args.get("language", ""),
        )
    elif name == "deep_search":
        return execute_deep_search(
            input_args.get("query", ""),
            input_args.get("repos", []),
            sg=sg,
        )
    elif name == "sourcegraph_search":
        return execute_sourcegraph_search(
            input_args.get("query", ""),
            sg=sg,
        )
    else:
        return f"Unknown tool: {name}"


