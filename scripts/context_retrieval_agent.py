def cross_validate_task(
    ctx: Dict[str, Any],
    repo_paths: Dict[str, Path],
    client: "anthropic.Anthropic",
    sg: Optional["SourcegraphClient"] = None,
    model: str = DEFAULT_MODEL,
    verbose: bool = False,
) -> Dict[str, Any]:
    """Run all backends on a single task and compare agreement.

    Returns a dict with per-backend results and agreement metrics.
    """
    results = {}
    backends_to_run = ["local", "hybrid"]
    if sg and sg.token:
        backends_to_run.append("deepsearch")

    for backend in backends_to_run:
        log.info("  Running backend: %s", backend)
        oracle, meta = run_agent(
            ctx, repo_paths, client,
            model=model, backend=backend, sg=sg, verbose=verbose,
        )
        results[backend] = {"oracle": oracle, "metadata": meta}

    # Compute pairwise file-level agreement
    agreement = {}
    backend_names = list(results.keys())
    for i, b1 in enumerate(backend_names):
        for b2 in backend_names[i + 1:]:
            files1 = {
                (f.get("repo", ""), f.get("path", ""))
                for f in results[b1]["oracle"].get("files", [])
            }
            files2 = {
                (f.get("repo", ""), f.get("path", ""))
                for f in results[b2]["oracle"].get("files", [])
            }
            intersection = files1 & files2
            union = files1 | files2
            jaccard = len(intersection) / len(union) if union else 1.0
            # F1-style agreement
            recall = len(intersection) / len(files1) if files1 else 1.0
            precision = len(intersection) / len(files2) if files2 else 1.0
            f1 = (2 * recall * precision / (recall + precision)) if (recall + precision) > 0 else 0.0
            agreement[f"{b1}_vs_{b2}"] = {
                "jaccard": round(jaccard, 4),
                "f1": round(f1, 4),
                "files_only_in_first": len(files1 - files2),
                "files_only_in_second": len(files2 - files1),
                "common_files": len(intersection),
            }

    # Also compare against existing oracle if available
    existing = ctx.get("existing_oracle", {})
    if existing and existing.get("files"):
        existing_files = {
            (f.get("repo", ""), f.get("path", ""))
            for f in existing.get("files", [])
        }
        for bname, bdata in results.items():
            agent_files = {
                (f.get("repo", ""), f.get("path", ""))
                for f in bdata["oracle"].get("files", [])
            }
            inter = existing_files & agent_files
            recall = len(inter) / len(existing_files) if existing_files else 1.0
            precision = len(inter) / len(agent_files) if agent_files else 1.0
            f1 = (2 * recall * precision / (recall + precision)) if (recall + precision) > 0 else 0.0
            agreement[f"{bname}_vs_existing_sg"] = {
                "f1": round(f1, 4),
                "recall_of_sg": round(recall, 4),
                "precision_vs_sg": round(precision, 4),
                "files_only_agent": len(agent_files - existing_files),
                "files_only_sg": len(existing_files - agent_files),
                "common": len(inter),
            }

    return {
        "task": f"{ctx.get('suite_name', '')}/{ctx.get('task_name', '')}",
        "results": {k: v["metadata"] for k, v in results.items()},
        "agreement": agreement,
        "oracles": {k: v["oracle"] for k, v in results.items()},
    }


def _extract_json_from_messages(messages: List[Dict]) -> Dict[str, Any]:
    """Extract the oracle JSON from the last assistant message."""
    # Walk backwards through messages to find the last assistant text
    for msg in reversed(messages):
        if msg.get("role") != "assistant":
            continue
        content = msg.get("content", "")
        # content might be a list of blocks or a string
        if isinstance(content, list):
            text_parts = []
            for block in content:
                if hasattr(block, "text"):
                    text_parts.append(block.text)
                elif isinstance(block, dict) and block.get("type") == "text":
                    text_parts.append(block.get("text", ""))
            text = "\n".join(text_parts)
        else:
            text = str(content)

        # Try to extract JSON from fenced block
        m = re.search(r"```json\s*\n(.*?)\n```", text, re.DOTALL)
        if m:
            try:
                return json.loads(m.group(1))
            except json.JSONDecodeError:
                pass

        # Try to find any JSON object
        m = re.search(r"\{[^{}]*\"files\"[^{}]*\}", text, re.DOTALL)
        if m:
            try:
                return json.loads(m.group(0))
            except json.JSONDecodeError:
                pass

        # Try the whole text
        try:
            return json.loads(text)
        except (json.JSONDecodeError, ValueError):
            pass

    return {"files": [], "text": "Agent did not produce valid JSON output."}


