def _validate_shell_command(command: str) -> Optional[str]:
    """Check that a shell command uses only allowed executables.

    Returns an error message if blocked, None if OK.
    """
    # Extract the first word (the executable)
    try:
        parts = shlex.split(command)
    except ValueError:
        parts = command.split()
    if not parts:
        return "Empty command"

    # Handle pipes: check each segment
    segments = command.split("|")
    for seg in segments:
        seg = seg.strip()
        if not seg:
            continue
        try:
            seg_parts = shlex.split(seg)
        except ValueError:
            seg_parts = seg.split()
        if not seg_parts:
            continue
        exe = seg_parts[0]
        # Strip path prefix
        exe = os.path.basename(exe)
        if exe not in ALLOWED_COMMANDS:
            return f"Command '{exe}' is not in the allowlist: {sorted(ALLOWED_COMMANDS)}"
    return None


def execute_run_shell(command: str, cwd: str = "") -> str:
    """Execute a read-only shell command."""
    err = _validate_shell_command(command)
    if err:
        return f"BLOCKED: {err}"
    try:
        result = subprocess.run(
            command, shell=True,
            capture_output=True, text=True,
            timeout=TOOL_TIMEOUT_SEC,
            cwd=cwd or None,
        )
        output = result.stdout
        if result.stderr:
            output += f"\n[stderr]: {result.stderr[:2000]}"
        if result.returncode != 0 and not output.strip():
            output = f"[exit code {result.returncode}]"
        return output[:10000]
    except subprocess.TimeoutExpired:
        return f"[TIMEOUT after {TOOL_TIMEOUT_SEC}s]"
    except Exception as e:
        return f"[ERROR]: {e}"


def execute_read_file(
    path: str, start_line: int = 0, end_line: int = 0
) -> str:
    """Read file contents."""
    p = Path(path)
    if not p.exists():
        return f"File not found: {path}"
    if not p.is_file():
        return f"Not a file: {path}"
    try:
        lines = p.read_text(errors="replace").splitlines()
    except Exception as e:
        return f"Error reading {path}: {e}"

    if start_line > 0:
        start_idx = max(0, start_line - 1)
        end_idx = end_line if end_line > 0 else start_idx + 500
        lines = lines[start_idx:end_idx]
        offset = start_idx + 1
    else:
        if len(lines) > 500:
            lines = lines[:500]
            lines.append(f"... [truncated at 500 lines, total {len(lines)} lines]")
        offset = 1

    numbered = [f"{i + offset}: {line}" for i, line in enumerate(lines)]
    return "\n".join(numbered)


def execute_list_directory(
    path: str, pattern: str = "", recursive: bool = False
) -> str:
    """List directory contents."""
    p = Path(path)
    if not p.exists():
        return f"Directory not found: {path}"
    if not p.is_dir():
        return f"Not a directory: {path}"

    try:
        if recursive:
            if pattern:
                entries = sorted(p.rglob(pattern))
            else:
                entries = sorted(p.rglob("*"))
            entries = entries[:2000]
        else:
            if pattern:
                entries = sorted(p.glob(pattern))
            else:
                entries = sorted(p.iterdir())

        lines = []
        for e in entries:
            rel = e.relative_to(p)
            suffix = "/" if e.is_dir() else ""
            lines.append(f"{rel}{suffix}")
        if len(lines) >= 2000:
            lines.append("... [truncated at 2000 entries]")
        return "\n".join(lines) if lines else "[empty directory]"
    except Exception as e:
        return f"Error: {e}"


def execute_search_imports(
    repo_path: str, import_pattern: str, language: str = ""
) -> str:
    """Language-aware import search using ripgrep."""
    lang = language.lower() if language else ""
    pattern = re.escape(import_pattern)

    # Build language-specific import regex patterns
    rg_patterns = []
    if lang in ("go", ""):
        rg_patterns.append(f'"({pattern}[^"]*)"')
    if lang in ("java", ""):
        rg_patterns.append(f"import\\s+.*{pattern}")
    if lang in ("python", ""):
        rg_patterns.append(f"(from|import)\\s+{pattern}")
    if lang in ("c", "cpp", ""):
        rg_patterns.append(f'#include\\s+[<"]{pattern}')
    if lang in ("rust", ""):
        rg_patterns.append(f"use\\s+{pattern}")
    if lang in ("javascript", "typescript", ""):
        rg_patterns.append(f"(import|require).*{pattern}")

    # Use the most specific pattern or combine
    if lang:
        search_pattern = rg_patterns[0] if rg_patterns else pattern
    else:
        search_pattern = "|".join(rg_patterns) if rg_patterns else pattern

    cmd = f"rg -n --no-heading '{search_pattern}' {shlex.quote(repo_path)}"
    try:
        result = subprocess.run(
            cmd, shell=True, capture_output=True, text=True,
            timeout=TOOL_TIMEOUT_SEC,
        )
        output = result.stdout[:10000]
        if not output.strip():
            # Fallback to simpler grep
            cmd2 = f"rg -n --no-heading '{pattern}' {shlex.quote(repo_path)}"
            result2 = subprocess.run(
                cmd2, shell=True, capture_output=True, text=True,
                timeout=TOOL_TIMEOUT_SEC,
            )
            output = result2.stdout[:10000]
        return output if output.strip() else f"No imports matching '{import_pattern}' found."
    except subprocess.TimeoutExpired:
        return f"[TIMEOUT searching for imports in {repo_path}]"
    except Exception as e:
        return f"[ERROR]: {e}"


def execute_find_symbols(
    repo_path: str, symbol_name: str, language: str = ""
) -> str:
    """Find symbol definitions using regex patterns."""
    name = re.escape(symbol_name)
    patterns = [
        # Go: func, type, interface
        f"(func|type|interface)\\s+{name}\\b",
        # Python: def, class
        f"(def|class)\\s+{name}\\b",
        # Java/C++: access modifiers + name
        f"(public|private|protected|static|abstract|final)?\\s*(class|interface|enum|struct|void|int|long|String|boolean)\\s+{name}\\b",
        # Rust: fn, struct, trait, impl, enum
        f"(fn|struct|trait|impl|enum|type|const)\\s+{name}\\b",
        # Generic assignment/definition
        f"\\b{name}\\s*[=:(]",
    ]
    combined = "|".join(f"({p})" for p in patterns)
    cmd = f"rg -n --no-heading -e '{combined}' {shlex.quote(repo_path)}"
    try:
        result = subprocess.run(
            cmd, shell=True, capture_output=True, text=True,
            timeout=TOOL_TIMEOUT_SEC,
        )
        return result.stdout[:10000] if result.stdout.strip() else f"No definition of '{symbol_name}' found."
    except subprocess.TimeoutExpired:
        return f"[TIMEOUT searching for {symbol_name}]"
    except Exception as e:
        return f"[ERROR]: {e}"


