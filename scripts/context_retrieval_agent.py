def main() -> int:
    parser = argparse.ArgumentParser(
        description="Context retrieval agent with multiple backends for oracle cross-validation"
    )
    # Task selection
    parser.add_argument(
        "--task-dir", type=str, default="",
        help="Single task directory to process",
    )
    parser.add_argument(
        "--suite", type=str, default="",
        help="Process all tasks in a suite (e.g., csb_org_crossrepo_tracing)",
    )
    parser.add_argument(
        "--sdlc-all", action="store_true",
        help="Process all SDLC tasks",
    )
    parser.add_argument(
        "--mcp-all", action="store_true",
        help="Process all org-scale tasks",
    )
    # Agent configuration
    parser.add_argument(
        "--model", type=str, default=DEFAULT_MODEL,
        help=f"Model to use (default: {DEFAULT_MODEL})",
    )
    parser.add_argument(
        "--backend", type=str, default="hybrid", choices=BACKENDS,
        help="Tool backend: local, deepsearch, or hybrid (default: hybrid)",
    )
    parser.add_argument(
        "--prompt-version", type=str, default="phase1",
        choices=("phase1", "v7"),
        help="Prompt version: phase1 (recall-focused, F1=0.749) or v7 (edit-centric). Default: phase1",
    )
    parser.add_argument(
        "--cross-validate", action="store_true",
        help="Run ALL backends on each task and report agreement metrics",
    )
    # Output
    parser.add_argument(
        "--output", type=str, default="",
        help="Override output file path (single-task mode only)",
    )
    parser.add_argument(
        "--output-dir", type=str, default="",
        help="Output directory for batch mode",
    )
    # Limits
    parser.add_argument(
        "--max-cost", type=float, default=0,
        help="Stop after spending this many USD (0 = unlimited)",
    )
    parser.add_argument(
        "--max-tasks", type=int, default=0,
        help="Process at most N tasks (0 = all). Useful for pilot runs.",
    )
    parser.add_argument(
        "--skip-existing", action="store_true",
        help="Skip tasks that already have an agent-generated oracle",
    )
    parser.add_argument(
        "--missing-only", action="store_true",
        help="Only process tasks that have NO ground truth at all (no oracle_answer.json, no ground_truth.json)",
    )
    parser.add_argument(
        "--no-verify", action="store_true",
        help="Skip dual-retrieval verification pass",
    )
    parser.add_argument(
        "--overwrite-existing", action="store_true",
        help="Overwrite existing ground_truth.json / oracle_answer.json (default: write to _agent variants)",
    )
    parser.add_argument(
        "--dry-run", action="store_true",
        help="Show tasks without running agent",
    )
    parser.add_argument(
        "--verbose", action="store_true",
        help="Verbose logging",
    )
    # Execution mode
    cli_group = parser.add_mutually_exclusive_group()
    cli_group.add_argument(
        "--use-cli", action="store_true", default=True,
        help="Use Claude CLI for subscription billing (default)",
    )
    cli_group.add_argument(
        "--use-sdk", action="store_true",
        help="Use Anthropic SDK directly (requires ANTHROPIC_API_KEY)",
    )
    args = parser.parse_args()
    use_cli = not args.use_sdk

    logging.basicConfig(
        level=logging.DEBUG if args.verbose else logging.INFO,
        format="%(asctime)s %(levelname)s %(message)s",
    )

    if use_cli:
        log.info("Using Claude CLI (subscription billing)")
    else:
        if not HAS_ANTHROPIC:
            log.error("anthropic package not installed. pip install anthropic")
            return 1

        # OAuth token preferred (subscription billing), API key fallback
        api_key = os.environ.get("CLAUDE_CODE_OAUTH_TOKEN", "")
        if api_key:
            log.info("Using OAuth token (CLAUDE_CODE_OAUTH_TOKEN)")
        else:
            api_key = os.environ.get("ANTHROPIC_API_KEY", "")
            if api_key:
                log.info("Using API key (ANTHROPIC_API_KEY)")
        if not api_key and not args.dry_run:
            log.error("Set CLAUDE_CODE_OAUTH_TOKEN or ANTHROPIC_API_KEY")
            return 1

    # Discover tasks
    tasks = discover_tasks(
        suite=args.suite,
        task_dir=args.task_dir,
        sdlc_all=args.sdlc_all,
        mcp_all=args.mcp_all,
    )
    if not tasks:
        log.error("No tasks found")
        return 1

    # Filter tasks based on flags
    original_count = len(tasks)
    if args.skip_existing or args.missing_only:
        filtered = []
        for t in tasks:
            tests_dir = t / "tests"
            suite = t.parent.name
            # Check for agent-generated oracle
            if args.skip_existing:
                if suite.startswith(("csb_org_", "ccb_mcp_")):
                    if (tests_dir / "oracle_answer_agent.json").exists():
                        continue
                else:
                    if (tests_dir / "ground_truth_agent.json").exists():
                        continue
            # Check for ANY ground truth
            if args.missing_only:
                has_any = (
                    (tests_dir / "oracle_answer.json").exists()
                    or (tests_dir / "ground_truth.json").exists()
                )
                if has_any:
                    continue
            filtered.append(t)
        tasks = filtered
        log.info("Filtered %d -> %d tasks (skip_existing=%s, missing_only=%s)",
                 original_count, len(tasks), args.skip_existing, args.missing_only)

    if args.max_tasks > 0:
        tasks = tasks[:args.max_tasks]

    log.info("Found %d tasks (model=%s, backend=%s, prompt=%s)",
             len(tasks), args.model, args.backend, args.prompt_version)

    if args.dry_run:
        for t in tasks:
            ctx = load_task_context(t)
            has_oracle = "existing_oracle" in ctx or "existing_ground_truth" in ctx
            print(f"  {'[HAS_GT]' if has_oracle else '[MISSING]'} {t.parent.name}/{t.name}")
        print(f"\nTotal: {len(tasks)} tasks")
        costs = MODEL_COSTS.get(args.model, MODEL_COSTS[DEFAULT_MODEL])
        est_cost = len(tasks) * 0.50  # rough estimate
        print(f"Estimated cost: ~${est_cost:.0f} ({args.model})")
        return 0

    client = None
    sg = None
    if not use_cli:
        client = anthropic.Anthropic(api_key=api_key)
        # Set up Sourcegraph client if needed (SDK mode only)
        if args.backend in ("deepsearch", "hybrid") or args.cross_validate:
            sg = SourcegraphClient()
            if not sg.token:
                if args.backend == "deepsearch":
                    log.error("SOURCEGRAPH_ACCESS_TOKEN required for deepsearch backend")
                    return 1
                log.warning("No SG token — deepsearch tools will be unavailable")
    cache_dir = get_cache_dir()

    total_cost = 0.0
    results_summary = []
    cross_val_results = []

    for i, task_dir in enumerate(tasks):
        if args.max_cost > 0 and total_cost >= args.max_cost:
            log.warning(
                "Cost limit reached ($%.2f >= $%.2f). Stopping.",
                total_cost, args.max_cost,
            )
            break

        log.info(
            "[%d/%d] Processing %s/%s",
            i + 1, len(tasks), task_dir.parent.name, task_dir.name,
        )

        ctx = parse_task_for_curator(task_dir)

        # Get repo paths (needed for local/hybrid backends)
        repo_paths = {}
        if args.backend != "deepsearch" or args.cross_validate:
            repo_paths = _resolve_repos(ctx, cache_dir)
            if not repo_paths and args.backend == "local":
                log.warning("  No repos available, skipping")
                continue

        if args.cross_validate:
            # Run all backends and compare
            try:
                cv_result = cross_validate_task(
                    ctx, repo_paths, client, sg=sg,
                    model=args.model, verbose=args.verbose,
                )
                cross_val_results.append(cv_result)
                # Sum costs across backends
                task_cost = sum(
                    m.get("cost_usd", 0) for m in cv_result["results"].values()
                )
                total_cost += task_cost
                log.info("  Agreement: %s", json.dumps(cv_result["agreement"], indent=2))
            except Exception as e:
                log.error("  Cross-validation failed: %s", e)
            continue

        # Single-backend run
        try:
            if use_cli:
                oracle, metadata = run_agent_cli(
                    ctx, repo_paths,
                    model=args.model, backend=args.backend,
                    verbose=args.verbose,
                    prompt_version=args.prompt_version,
                )
            else:
                oracle, metadata = run_agent(
                    ctx, repo_paths, client,
                    model=args.model, backend=args.backend,
                    sg=sg, verbose=args.verbose,
                )
        except Exception as e:
            log.error("  Agent failed: %s", e)
            continue

        total_cost += metadata.get("cost_usd", 0)

        # Write output — use curator outputs (ground_truth + oracle + meta)
        output_path = ""
        if args.output and len(tasks) == 1:
            output_path = args.output
        elif args.output_dir:
            out_dir = Path(args.output_dir) / task_dir.parent.name
            out_dir.mkdir(parents=True, exist_ok=True)
            suite = task_dir.parent.name
            if suite.startswith(("csb_org_", "ccb_mcp_")):
                output_path = str(out_dir / f"{task_dir.name}_oracle_agent.json")
            else:
                output_path = str(out_dir / f"{task_dir.name}_gt_agent.json")

        # Dual-retrieval verification (unless --no-verify)
        if not args.no_verify:
            vr = verify_dual_retrieval(oracle, repo_paths, sg_client=sg)
            metadata["dual_verification"] = vr["summary"]

        outputs = write_curator_outputs(
            task_dir, oracle, metadata, ctx,
            output_path=output_path,
            overwrite=args.overwrite_existing,
        )

        n_files = len(oracle.get("files", []))
        n_symbols = len(oracle.get("symbols", []))
        n_chunks = len(oracle.get("chunks", []))
        results_summary.append({
            "task": f"{task_dir.parent.name}/{task_dir.name}",
            "files_found": n_files,
            "symbols_found": n_symbols,
            "chunks_found": n_chunks,
            "cost_usd": metadata["cost_usd"],
            "tool_calls": metadata["tool_calls"],
            "elapsed_sec": metadata["elapsed_sec"],
            "outputs": {k: str(v) for k, v in outputs.items()},
        })

        log.info(
            "  -> %d files, %d symbols, %d chunks, $%.4f, %d tool calls, %.1fs",
            n_files, n_symbols, n_chunks, metadata["cost_usd"],
            metadata["tool_calls"], metadata["elapsed_sec"],
        )

    # Summary
    print(f"\n{'=' * 60}")
    if args.cross_validate and cross_val_results:
        print(f"Cross-validated: {len(cross_val_results)} tasks")
        print(f"Total cost: ${total_cost:.4f}")
        # Aggregate agreement
        for pair_key in cross_val_results[0].get("agreement", {}):
            f1s = [
                r["agreement"][pair_key]["f1"]
                for r in cross_val_results
                if pair_key in r.get("agreement", {})
            ]
            if f1s:
                avg_f1 = sum(f1s) / len(f1s)
                print(f"  {pair_key}: avg F1 = {avg_f1:.4f} (n={len(f1s)})")
        # Write detailed results
        cv_report_path = Path(args.output_dir or ".") / "cross_validation_report.json"
        cv_report_path.write_text(json.dumps(cross_val_results, indent=2) + "\n")
        print(f"\nDetailed report: {cv_report_path}")
    else:
        print(f"Processed: {len(results_summary)}/{len(tasks)} tasks")
        print(f"Total cost: ${total_cost:.4f}")
        if results_summary:
            avg_files = sum(r["files_found"] for r in results_summary) / len(results_summary)
            avg_cost = total_cost / len(results_summary)
            print(f"Avg files/task: {avg_files:.1f}")
            print(f"Avg cost/task: ${avg_cost:.4f}")
    print(f"{'=' * 60}")

    return 0


def _resolve_repos(
    ctx: Dict[str, Any], cache_dir: Path
) -> Dict[str, Path]:
    """Resolve repos for a task — clone if needed, return path mapping."""
    repo_paths: Dict[str, Path] = {}

    # Strategy 1: Use repo fixture (org-scale tasks)
    fixture = ctx.get("repo_fixture")
    if fixture:
        specs = fixture.get("repos", [])
        return ensure_repos_cloned(specs, cache_dir)

    # Strategy 2: Extract from Dockerfile clone URLs
    clone_urls = ctx.get("repo_urls", [])
    for entry in clone_urls:
        slug = entry.get("slug", "")
        target = entry.get("target", "")
        if slug:
            try:
                path = clone_repo(slug, cache_dir)
                # Use both slug and target as keys
                repo_paths[slug] = path
                if target and target != ".":
                    repo_paths[target] = path
            except Exception as e:
                log.warning("Failed to clone %s: %s", slug, e)

    if repo_paths:
        return repo_paths

    # Strategy 3-5: Parse Dockerfile comments and TAC image mapping
    task_dir = Path(ctx.get("task_dir", ""))
    dockerfile = task_dir / "environment" / "Dockerfile"
    if dockerfile.exists():
        text = dockerfile.read_text()

        # Strategy 3: # Repo: comment (SWEAP images)
        m = re.search(r"#\s*Repo:\s*(\S+)", text)
        if m:
            slug = m.group(1).strip()
            log.info("  Resolved repo from Dockerfile # Repo: %s", slug)
            try:
                path = clone_repo(slug, cache_dir)
                repo_paths[slug] = path
            except Exception as e:
                log.warning("Failed to clone %s: %s", slug, e)

        # Strategy 4: # Source: org/repo (commit) (SWEAP debug tasks)
        if not repo_paths:
            m = re.search(r"#\s*Source:\s*(\S+)\s+\(([a-f0-9]+)\)", text)
            if m:
                slug = m.group(1).strip()
                log.info("  Resolved repo from Dockerfile # Source: %s", slug)
                try:
                    path = clone_repo(slug, cache_dir)
                    repo_paths[slug] = path
                except Exception as e:
                    log.warning("Failed to clone %s: %s", slug, e)

        # Strategy 4b: Parse SWEAP FROM tag when # Source: uses instance ID format
        # Tag: jefzda/sweap-images:org.repo-org__repo-commitHash-version
        if not repo_paths:
            m = re.search(
                r"jefzda/sweap-images:([\w-]+)\.([\w-]+)-[\w_]+-([a-f0-9]{10,})",
                text,
            )
            if m:
                slug = f"{m.group(1)}/{m.group(2)}"
                log.info("  Resolved repo from SWEAP FROM tag: %s", slug)
                try:
                    path = clone_repo(slug, cache_dir)
                    repo_paths[slug] = path
                except Exception as e:
                    log.warning("Failed to clone %s: %s", slug, e)

    # Strategy 5: TAC image task name → known repo mapping
    if not repo_paths:
        _TAC_REPO_MAP = {
            "bustub": "cmu-db/bustub",
            "openhands": "All-Hands-AI/OpenHands",
        }
        task_name = ctx.get("task_name", "")
        for key, slug in _TAC_REPO_MAP.items():
            if key in task_name.lower():
                log.info("  Resolved repo from TAC map: %s -> %s", task_name, slug)
                try:
                    path = clone_repo(slug, cache_dir)
                    repo_paths[slug] = path
                except Exception as e:
                    log.warning("Failed to clone %s: %s", slug, e)
                break

    return repo_paths


if __name__ == "__main__":
    sys.exit(main())
