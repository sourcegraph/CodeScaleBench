def get_cache_dir() -> Path:
    """Get the repo clone cache directory."""
    d = Path(os.environ.get("CCB_REPO_CACHE", str(DEFAULT_CACHE_DIR)))
    d.mkdir(parents=True, exist_ok=True)
    return d


def clone_repo(mirror_slug: str, cache_dir: Optional[Path] = None) -> Path:
    """Clone a repo to the cache. Skip if already exists.

    Args:
        mirror_slug: e.g. "sg-evals/kubernetes--v1.32.0"
        cache_dir: Override cache directory.

    Returns:
        Path to the cloned repo.
    """
    cache = cache_dir or get_cache_dir()
    # Use the slug as directory name (replace / with __)
    dir_name = mirror_slug.replace("/", "__")
    repo_dir = cache / dir_name

    if repo_dir.exists() and (repo_dir / ".git").exists():
        log.debug("Cache hit: %s", repo_dir)
        return repo_dir

    url = f"https://github.com/{mirror_slug}.git"
    log.info("Cloning %s -> %s", url, repo_dir)
    repo_dir.mkdir(parents=True, exist_ok=True)
    try:
        subprocess.run(
            ["git", "clone", "--depth", "1", url, str(repo_dir)],
            check=True, capture_output=True, text=True,
            timeout=600,
        )
    except subprocess.CalledProcessError as e:
        log.error("Clone failed for %s: %s", mirror_slug, e.stderr[:500])
        raise
    return repo_dir


def ensure_repos_cloned(
    repo_specs: List[Dict[str, str]], cache_dir: Optional[Path] = None
) -> Dict[str, Path]:
    """Clone all repos and return a mapping of logical_name -> local_path."""
    result = {}
    for spec in repo_specs:
        full_name = spec.get("full_name", "")
        logical = spec.get("logical_name", full_name)
        try:
            path = clone_repo(full_name, cache_dir)
            result[logical] = path
            result[full_name] = path  # also map by full_name
        except Exception as e:
            log.warning("Failed to clone %s: %s", full_name, e)
    return result


