def find_project_root() -> Path:
    """Walk up from this script to find the CodeContextBench root."""
    p = Path(__file__).resolve().parent
    while p != p.parent:
        if (p / "benchmarks").is_dir() and (p / "fixtures").is_dir():
            return p
        p = p.parent
    return Path(__file__).resolve().parent.parent


def discover_tasks(
    suite: str = "",
    task_dir: str = "",
    sdlc_all: bool = False,
    mcp_all: bool = False,
) -> List[Path]:
    """Discover task directories to process."""
    root = find_project_root()
    benchmarks = root / "benchmarks"

    if task_dir:
        p = Path(task_dir)
        if not p.is_absolute():
            p = root / p
        return [p] if p.is_dir() else []

    suites = []
    if sdlc_all:
        suites = SDLC_SUITES
    elif mcp_all:
        suites = MCP_UNIQUE_SUITES
    elif suite:
        suites = [suite]
    else:
        suites = SDLC_SUITES + MCP_UNIQUE_SUITES

    tasks = []
    for s in suites:
        suite_dir = benchmarks / s
        if not suite_dir.is_dir():
            continue
        for task in sorted(suite_dir.iterdir()):
            if task.is_dir() and not task.name.startswith("."):
                tasks.append(task)
    return tasks


def load_task_context(task_dir: Path) -> Dict[str, Any]:
    """Load all relevant context from a task directory.

    Returns dict with: instruction, seed_prompt, repo_set_id, check_types,
    repo_specs, existing_oracle, suite_name.
    """
    root = find_project_root()
    ctx: Dict[str, Any] = {
        "task_dir": str(task_dir),
        "task_name": task_dir.name,
        "suite_name": task_dir.parent.name,
    }

    # Read instruction.md
    instruction = task_dir / "instruction.md"
    if instruction.exists():
        ctx["instruction"] = instruction.read_text()[:8000]
    else:
        ctx["instruction"] = ""

    # Try task_spec.json (MCP-unique tasks)
    task_spec_path = task_dir / "tests" / "task_spec.json"
    if not task_spec_path.exists():
        task_spec_path = task_dir / "task_spec.json"

    if task_spec_path.exists():
        try:
            spec = json.loads(task_spec_path.read_text())
            ctx["seed_prompt"] = spec.get("prd", {}).get("seed_prompt", "")
            ctx["repo_set_id"] = spec.get("artifacts", {}).get("repo_set_id", "")
            ctx["check_types"] = [
                c.get("type", "") for c in spec.get("evaluation", {}).get("checks", [])
            ]
        except (json.JSONDecodeError, KeyError):
            pass

    # Try to get repo info from Dockerfile
    dockerfile = task_dir / "environment" / "Dockerfile"
    if dockerfile.exists():
        ctx["repo_urls"] = _extract_clone_urls(dockerfile.read_text())

    # Load repo set fixture if available
    repo_set_id = ctx.get("repo_set_id", "")
    if repo_set_id:
        fixture_path = root / "fixtures" / "repo_sets" / f"{repo_set_id}.json"
        if fixture_path.exists():
            try:
                ctx["repo_fixture"] = json.loads(fixture_path.read_text())
            except json.JSONDecodeError:
                pass

    # Load existing oracle for comparison
    oracle_path = task_dir / "tests" / "oracle_answer.json"
    if oracle_path.exists():
        try:
            ctx["existing_oracle"] = json.loads(oracle_path.read_text())
        except json.JSONDecodeError:
            pass

    gt_path = task_dir / "tests" / "ground_truth.json"
    if gt_path.exists():
        try:
            ctx["existing_ground_truth"] = json.loads(gt_path.read_text())
        except json.JSONDecodeError:
            pass

    return ctx


def _extract_clone_urls(dockerfile_content: str) -> List[Dict[str, str]]:
    """Extract git clone URLs from a Dockerfile."""
    results = []
    for match in re.finditer(
        r"git\s+clone\s+(?:--depth\s+\d+\s+)?(\S+?)(?:\.git)?\s+(\S+)",
        dockerfile_content,
    ):
        url = match.group(1)
        target = match.group(2)
        # Extract mirror slug from URL
        m = re.search(r"github\.com/(.+?)(?:\.git)?$", url)
        slug = m.group(1) if m else url
        results.append({"url": url, "slug": slug, "target": target})
    return results


